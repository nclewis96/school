/**
  * Return the larger of two given integers.
  * @param x An Integer
  * @param y An Integer
  * @return The larger of the two integers
  */

#include <iostream>
using namespace std;

int computeMax(int x, int y);	// A Function Prototype

int main()
{
   int a = 0;
   int b = 0;
   cout << "Please enter two integers: ";
   cin >> a >> b;

   int largerAB = computeMax(a, b);
   cout << "The larger of " << a << " and " << b << " is " << largerAB << ".\n";
} // End Main

int computeMax (int x, int y)
{
   return (x > y) ? x : y;	// Conditional Operator
} // End computeMax



